function t(key, vars = {}) {
  const lang = localStorage.getItem('dse_lang') || 'zh';
  const dict = I18N[lang] || I18N.zh;
  let text = dict[key] || I18N.zh[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  }
  return text;
}
